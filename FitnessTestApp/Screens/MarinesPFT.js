class MarinePFT extends React.Component {
  constructor (props) {
    super (props);
    this.state = {
      exChoice: true,
      push: false, 
      gender: false,
      male: true,
      age: 0,
      pushUps: 0,
      crunches: 0,
      pullUps: 0,
      run: 0,
      stopwatch: true
    }
  }

  scoreCalc = (reps, station) => {
    let points = 0;
    let ageGroup = 1;
    let gend;
    let scoreChart;
    station === "Push Ups" ? scoreChart = pushUpScoreChart : station === "Pull Ups" ? scoreChart = pullUpScoreChart : station === "Crunches" ? scoreChart = sitUpScoreChart : scoreChart = runScoreChart
    this.state.male ? gend = "male" : gend = "female"
    const age = parseInt(this.state.age)
    switch (true) {
      case (age >= 51):
        ageGroup = 8;
        break;
      case (age >= 46 && age < 51):
        ageGroup = 7;
        break;
      case (age >= 41 && age < 46):
        ageGroup = 6;
        break;
      case (age >= 36 && age < 41):
        ageGroup = 5;
        break;
      case (age >= 31 && age < 36):
        ageGroup = 4;
        break;
      case (age >= 26 && age < 31):
        ageGroup = 3;
        break;
      case (age >= 21 && age < 26):
        ageGroup = 2;
        break;
      default:
        ageGroup = 1;
    }

    station === "3 Mile Run" ? (reps < Math.min(... Object.keys(scoreChart[gend][ageGroup])) ? points = 100 : (reps > Math.max(... Object.keys(scoreChart[gend][ageGroup])) ? points = 0 : points = scoreChart[gend][ageGroup][reps])) : 
    (Object.keys(scoreChart[gend][ageGroup]).forEach((key) => {
      scoreChart[gend][ageGroup][key].includes(parseInt(reps)) ? points = key : points = points
    }))

    station === "Push Ups" ? (this.setState({
      pushUps: points,
      pullUps: 0
    })) : station === "Pull Ups" ? (this.setState({
      pullUps: points,
      pushUps: 0
    })) : station === "Crunches" ? (this.setState({
      crunches: points
    })) : (this.setState({
      run: points
    }))

    return points;
  }

  toggleTimer = () => {
    this.setState({stopwatch: !this.state.stopwatch})
  }

  toggleSwitch = () => {
    this.setState({
      exChoice: !this.state.exChoice,
      push: !this.state.push
    }, () => console.log(this.state.push))
  }

  toggleGender = () => {
    this.setState({
      gender: !this.state.gender,
      male: !this.state.male
    }, () => console.log(this.state.male))
    
}

  render () {
      return (
          <View style={styles.container}>
            <Text style={styles.text}>US Marines PFT</Text>
            {this.state.stopwatch ? (<Stopwatch />) : (<Timer />)} 
            {this.state.stopwatch ? (<Buttons func={this.toggleTimer} name={"Timer"} />) : (<Buttons func={this.toggleTimer} name={"Stopwatch"} />)}
            <View style={styles.switch}>
              <CheckBox
                center
                title='Push Ups'
                checkedIcon='dot-circle-o'
                uncheckedIcon='circle-o'
                checked={this.state.push}
                onPress={() => {this.state.push ? this.setState() : this.toggleSwitch()}}
              />
              <CheckBox
                center
                title='Pull Ups'
                checkedIcon='dot-circle-o'
                uncheckedIcon='circle-o'
                checked={this.state.exChoice}
                onPress={() => {this.state.push ? this.toggleSwitch() : this.setState()}}
              />
            </View>
            <View style={styles.switch}>
              <CheckBox
                center
                title='Male'
                checkedIcon='dot-circle-o'
                uncheckedIcon='circle-o'
                checked={this.state.male}
                onPress={() => {this.state.male ? this.setState() : this.toggleGender()}}
              />
              <CheckBox
                center
                title='Female'
                checkedIcon='dot-circle-o'
                uncheckedIcon='circle-o'
                checked={this.state.gender}
                onPress={() => {this.state.male ? this.toggleGender() : this.setState()}}
              />
            </View>
            <View style={styles.switch}>
              <Text>Age: </Text>
              <RNPickerSelect 
                  placeholder={{label: "Age", value: "Age"}}
                  onValueChange={(itemValue, itemIndex) =>
                      this.setState({age: itemValue})}
                  items={
                      range(100).map((age) => ({
                          label: age.toString(), 
                          value: age.toString(), 
                          key: age.toString()
                          }))} 
              />
            </View>   
            {this.state.exChoice ? (<StationInput station={"Pull Ups"} calc={this.scoreCalc} pf={true} max={51} age={this.state.age} gender={this.state.gender} ex={this.state.push }/>) : (<StationInput station={"Push Ups"} calc={this.scoreCalc} pf={true} max={101} age={this.state.age} gender={this.state.gender} ex={this.state.push} />)}
            <StationInput station={"Crunches"} calc={this.scoreCalc} pf={true} max={151} age={this.state.age} gender={this.state.gender} />
            <TimeInput station={"3 Mile Run"} calc={this.scoreCalc} pf={true} age={this.state.age} gender={this.state.gender} /> 
            {(this.state.pushUps >= 40 || this.state.pullUps >= 40) && this.state.run && this.state.crunches >= 40 ? 
                (<Text style={styles.pass}>Result: PASS</Text>) : (<Text style={styles.fail}>Result: FAIL</Text>)}
          </View>
      )
  }
}

